export async function processMessage(userMessage, chatHistory = []) {
  try {
    // Format previous history for OpenAI context
    const messages = [
      { role: "system", content: SYSTEM_PROMPT },
      ...chatHistory.map(msg => ({
        role: msg.direction === 'incoming' ? 'user' : 'assistant',
        content: msg.content
      })),
      { role: "user", content: userMessage }
    ];

    const completion = await openai.chat.completions.create({
      model: "gpt-4o",
      messages: messages,
      temperature: 0.6,
      response_format: { type: "json_object" }
    });

    const responseContent = completion.choices[0].message.content;
    const parsedResponse = JSON.parse(responseContent);

    return parsedResponse;

  } catch (error) {
    console.error("Error calling OpenAI:", error);
    return {
      intent: "GENERAL",
      replyMessage: "عذراً، واجهت مشكلة تقنية بسيطة. ممكن تعيد رسالتك مرة ثانية؟",
      leadData: { name: null, phone: null, industry: null, bottleneck: null }
    };
  }
}
